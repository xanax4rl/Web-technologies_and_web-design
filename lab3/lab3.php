/* -------- 1. Парні числа від 1 до 20 (while) -------- */
$i = 1;
echo "Завдання 1:<br>";
echo "Парні числа від 1 до 20: ";
while ($i <= 20) {
    if ($i % 2 === 0) {
        echo $i . " ";
    }
    $i++;
}
echo "<br>";

/* -------- 2. Найбільше з трьох чисел (if-else) -------- */
$a = 12;
$b = 25;
$c = 18;

if ($a >= $b && $a >= $c) {
    $max = $a;
} elseif ($b >= $a && $b >= $c) {
    $max = $b;
} else {
    $max = $c;
}
echo "Завдання 2:<br>";
echo "Найбільше з чисел $a, $b, $c: $max";
