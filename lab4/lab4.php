/* ---------- 1. Об'єднання масивів у асоціативний ---------- */
$names = ["Андрій", "Марія", "Петро", "Ірина"];
$ages  = [25, 30, 28, 22];

$people = array_combine($names, $ages); // ключ — ім'я, значення — вік

echo "Завдання 1:<br>"; 
echo "<strong>Асociативний масив (ім'я ⇒ вік):</strong><br>";
print_r($people);
echo "<br>";
/* ---------- 2. Функція середнього значення ---------- */
function average(array $arr): float
{
    return count($arr) ? array_sum($arr) / count($arr) : 0;
}

$numbers = range(1, 10);  // масив 1..10
$avg = average($numbers);

echo "<br>Завдання 2:"; 

echo "<br><strong>Масив чисел:</strong><br>";
print_r($numbers);

echo "<br><strong>Середнє значення:</strong> $avg";
