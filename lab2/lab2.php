/* ------------------ 1. Площа трапеції ------------------ */
// вихідні дані
$a = 9;   // довжина більшої основи
$b = 6;   // довжина меншої основи (можна змінити)
$h = 4;   // висота

// формула: S = (a + b) * h / 2
$area = ($a + $b) * $h / 2;
echo "Завдання 1:<br>";
echo "Площа трапеції (a=$a, b=$b, h=$h): $area<br>";

/* --------------- 2. Тип і приведення до int --------------- */
$value = "123.45";              // початкове значення (string)
$origType = gettype($value);    // початковий тип

$intValue = (int)$value;        // явне приведення до цілого
$convType = gettype($intValue); // тип після приведення
echo "Завдання 2:<br>";
echo "Початковий тип \"{$value}\" — {$origType}<br>";
echo "Після перетворення: {$intValue} ({$convType})";
?>